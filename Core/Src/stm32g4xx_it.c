/* USER CODE BEGIN 1 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  if(htim->Instance==TIM2 && htim->Channel==HAL_TIM_ACTIVE_CHANNEL_4)
  {
    uint32_t capture=HAL_TIM_ReadCapturedValue(&htim2, TIM_CHANNEL_4);
    uint32_t diff;
    if(capture>=ppm_last_capture)
    {
      diff=capture-ppm_last_capture;
    }
    else
    {
      diff=(65536+capture)-ppm_last_capture;
    }
    ppm_last_capture=capture;
    if(diff>2500)
    {
      if(ppm_channel_indx==PPM_CHANNELS)
      {
        for(uint8_t i=0;i<PPM_CHANNELS;i++)
        {
          ppm_recv[i]=ppm_buf[i];
        }
        ppm_flag=1;
      }
      ppm_channel_indx=0;
    }
    else if(diff>=800 && diff<=2200)
    {
      if(ppm_channel_indx<PPM_CHANNELS)
      {
        ppm_buf[ppm_channel_indx]=(uint16_t)diff;
        ppm_channel_indx++;
      }
    }
  }
}
